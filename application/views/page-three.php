			data['option-item'] = $(this).find('option:selected').text();
			changeObject(data);
		});
		$('input:radio[name=code]').change(function () {
			data['code'] = $(this).val();
			changeObject(data);
		});
		$('input:radio[name=isps]').change(function () {
			data['isps'] = $(this).val();
			changeObject(data);
